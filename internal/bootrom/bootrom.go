func NewBootRom(cgb bool) *BootRom {
	var bootrom []uint8
	if cgb {
		logger.Info("Using CGB bootrom")
		bootrom = make([]uint8, len(cgbBootROM))
		copy(bootrom, cgbBootROM)
	} else {
		logger.Info("Using DMG bootrom")
		bootrom = make([]uint8, len(dmgBootROM))
		copy(bootrom, dmgBootROM)
	}

	return &BootRom{
		bootrom:   bootrom,
		IsEnabled: true,
	}
}

type BootRom struct {
	bootrom   []uint8
	IsEnabled bool
}

func (br *BootRom) GetItem(addr uint16) uint8 {
	return br.bootrom[addr]
}

func (br *BootRom) Enabled() bool {
	return br.IsEnabled
}

func (br *BootRom) Disable() {
	br.IsEnabled = false
}

func (br *BootRom) Enable() {
	br.IsEnabled = true
}
